# Arithmetic Operator
a = 3
b = 4
c = a * b
print(c)

# Assignment Operator

a = 9 - 2  # value is assigning to a

print(a)

b = 7

a += 4  # b will be increment by 4 and save into b
b -= 4  # b will be decrement by 4 and save into b

print(a)
print(b)

# Comparison Operator

d = 9 < 3
e = 4 >= 4
f = 5 == 5
g = 3 != 4
print(d)
print(e)
print(f)
print(g)

# Logical Operator

x = True or False
y = True and False
print(x)
print(y)

# Print truth table of 'or'

print("True or False is -> ", True or False)
print("True or True is -> ", True or True)
print("False or False is -> ", False or False)
print("False or True is -> ", False or True)

# Print truth table of 'and'

print("True and False is -> ", True and False)
print("True and True is -> ", True and True)
print("False and False is -> ", False and False)
print("False and True is -> ", False and True)

print(not (True))  # apply only on boolean variables
