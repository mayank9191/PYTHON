a = "mayank is good boy\nbut not bad boy."  # create new line

print(a)

a = "mayank is good boy\tbut not bad boy."  # create tab space

print(a)

a = "mayank is good boy but not \'bad boy\'."  # create single quote

print(a)

a = "mayank is good boy \\ but not bad boy."  # create backslash

print(a)

a = "mayank is good boy \v but not bad boy."  # create vertical tab space

print(a)

a = "mayank is good boy \b but not bad boy."  # create vertical tab space

print(a)
