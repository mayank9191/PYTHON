name = 'Mayank'      # Single quoted string
name = "Mayank"      # Double quoted string
name = '''Mayank'''  # Triple quoted string

# STRING SLICING

# positive slicing

print(len(name))    # use to print length of string(number of character in string)

nameshort = name[0:3]  # start from index 0 all the way till 3 (excluding 3)()

print(nameshort)

character1 = name[0]

print(character1)
